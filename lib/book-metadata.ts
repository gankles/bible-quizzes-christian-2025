export function getBookMetadata(slug: string): BookMetadata | undefined {
  return BOOK_METADATA[slug.toLowerCase()];
}

export function getAllBookMetadata(): BookMetadata[] {
  return Object.values(BOOK_METADATA);
}

export function getBooksByTestament(testament: 'old' | 'new'): BookMetadata[] {
  return Object.values(BOOK_METADATA).filter(b => b.testament === testament);
}

export function getBooksByCategory(category: string): BookMetadata[] {
  return Object.values(BOOK_METADATA).filter(b => 
    b.category.toLowerCase().includes(category.toLowerCase())
  );
}
